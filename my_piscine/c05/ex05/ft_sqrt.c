int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 1000000)
	{
		while (i < 1000)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	if (nb > 1000000)
	{
		i = 1000;
		while (i < 47000)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
