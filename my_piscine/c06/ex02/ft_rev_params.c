#include <unistd.h>

int	main(int argc, char **argv)
{
	int	k;
	int	j;

	j = 0;
	k = argc -1;
	if (argc > 1)
	{
		while (k >= 1)
		{
			while (argv[k][j] != '\0' && k >= 1)
			{
				write(1, &argv[k][j], 1);
				j++;
			}
			write(1, "\n", 1);
			k--;
			j = 0;
		}
	}
}
