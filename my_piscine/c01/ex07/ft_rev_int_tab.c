void	ft_rev_int_tab(int *tab, int size)
{
	int	*i;
	int	*f;
	int	temp;

	*i = tab;
	*f = tab + (size -1);
	while (i < f)
	{
		temp = *i;
		*i = *f;
		*f = temp;
		i++;
		f--;
	}
}
